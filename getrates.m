function [rates,v,step,N]=getrates
N=[0.5 0.49 0.01];
v=0.4;
cvg=0.2;
step=1/52;
ac=1;
avd=@(x,n) (1-x).^n;
betam=0.01;
betaf=0.01;
rec=1;
probcm=avd(betam,ac*step);
probcf=avd(betaf,ac*step);
ar=100;
probrm=avd(betam,ar*step);
probrf=avd(betaf,ar*step);
locf=0.8;
locs=0.6;
numcasm=4;
numcass=150;
numcasf=(numcasm*N(1)-numcass*N(3)*locs)./N(2)/locf;
numregm=0.8;
numregs=1;
numregf=(numregm*N(1)-numregs*N(3))./N(2);
rates(1)=log(probcm).*numcasm+log(probrm).*numregm;
rates(2)=log(probcf).*numcasf+log(probrf).*numregf;
rates(3)=log(probcf).*numcass+log(probrf).*numregs;
rates(4)=rec*step;
rates(5)=(numcasf.*locf.*log(probcm)+numregf.*log(probrm))./rates(1);
rates(6)=locf;
rates(7)=locs;
rates(8)=cvg;